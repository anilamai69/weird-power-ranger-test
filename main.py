def on_on_update():
    global Rotation2
    if controller.acceleration(ControllerDimension.X) < 150:
        Rotation2 += -0.5
    if controller.acceleration(ControllerDimension.X) > -150:
        Rotation2 += 0.5
    if controller.acceleration(ControllerDimension.X) < 250:
        Rotation2 += -0.5
    if controller.acceleration(ControllerDimension.X) > -250:
        Rotation2 += 0.5
    if controller.acceleration(ControllerDimension.X) < 350:
        Rotation2 += -0.5
    if controller.acceleration(ControllerDimension.X) > -350:
        Rotation2 += 0.5
    if controller.acceleration(ControllerDimension.X) < 450:
        Rotation2 += -0.5
    if controller.acceleration(ControllerDimension.X) > -450:
        Rotation2 += 0.5
    if controller.acceleration(ControllerDimension.X) < 550:
        Rotation2 += -0.5
    if controller.acceleration(ControllerDimension.X) > -550:
        Rotation2 += 0.5
    if controller.acceleration(ControllerDimension.X) < 650:
        Rotation2 += -0.5
    if controller.acceleration(ControllerDimension.X) > -650:
        Rotation2 += 0.5
    if controller.acceleration(ControllerDimension.X) < 750:
        Rotation2 += -0.5
    if controller.acceleration(ControllerDimension.X) > -750:
        Rotation2 += 0.5
    if controller.acceleration(ControllerDimension.X) < 850:
        Rotation2 += -0.5
    if controller.acceleration(ControllerDimension.X) > -850:
        Rotation2 += 0.5
    if controller.acceleration(ControllerDimension.X) < 950:
        Rotation2 += -0.5
    if controller.acceleration(ControllerDimension.X) > -950:
        Rotation2 += 0.5
    Render.set_view_angle_in_degree(Rotation2)
    spriteutils.place_angle_from(mySprite,
        spriteutils.degrees_to_radians(Rotation2),
        -16,
        mySprite2)
    PlayerLoc.set_position(mySprite2.x, mySprite2.y)
    if controller.dy() == 0:
        pass
    else:
        spriteutils.place_angle_from(mySprite2,
            spriteutils.degrees_to_radians(Rotation2),
            controller.dy(-25),
            PlayerLoc)
    if controller.dx() == 0:
        pass
    else:
        spriteutils.place_angle_from(mySprite2,
            spriteutils.degrees_to_radians(Rotation2 + 90),
            controller.dx(25),
            PlayerLoc)
game.on_update(on_on_update)
